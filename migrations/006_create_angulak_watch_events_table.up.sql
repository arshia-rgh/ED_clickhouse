CREATE TABLE IF NOT EXISTS angulak_watch_events (
    event_id String,
    event_name String,
    user_id String,
    session_id String,
    anonymous_id String,
    timestamp DateTime,
    service_origin String,
    platform String,
    platform_version String,
    os_name String,
    os_version String,
    browser_name String,
    browser_version String,
    device_type String,
    screen_resolution String,
    user_agent String,
    state String,
    item_type String,
    item_id String,
    play_info_id String,
    season_number Int32,
    episode_number Int32,
    subtitle_language String,
    audio_language String,
    video_position Int32,
    video_duration Int32,
    player_version String,
    internet_connection_type String,
    region String,
    ad_id String,
    ad_type String,
    event_details String
) ENGINE = MergeTree()
ORDER BY
    (timestamp, event_name);